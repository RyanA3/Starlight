package me.felnstaren.starlight.engine.graphics;

public class Color {

	public int r = 0;
	public int g = 0;
	public int b = 0;
	public int a = 0;
	
	public Color(int r, int g, int b, int a) {
		this.r = r;
		this.g = g;
		this.b = b;
		this.a = a;
	}
	
	public Color(int r, int g, int b) {
		this.r = r;
		this.g = g;
		this.b = b;
	}
	
}
